CREATE TABLE Clientes (
    ClienteID INT PRIMARY KEY,
    Cliente VARCHAR(255),
    NomeContato VARCHAR(255),
    Endereco VARCHAR(255),
    Cidade VARCHAR(255),
    Pais VARCHAR(255),
    PaisCodigo INT,
    Regiao VARCHAR(255),
    CEP VARCHAR(255),
    Latitude FLOAT,
    Longitude FLOAT,
    Fax VARCHAR(255),
    Telefone VARCHAR(255)
);
